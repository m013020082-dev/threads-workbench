import { useState, useEffect } from 'react';
import { Search as SearchIcon, User, ThumbsUp, MessageCircle, Repeat2, Sparkles, ListPlus, ExternalLink, ChevronDown, ChevronUp, Pencil, Check, X, Loader2, Send, CheckCircle2, XCircle } from 'lucide-react';
import clsx from 'clsx';
import { Post, RankingResult, Draft } from '../types';
import { DraftCard } from './DraftCard';
import { updatePost, radarExecuteDirect } from '../api/client';

interface PostListProps {
  results: RankingResult[];
  isLoading: boolean;
  onGenerateDrafts: (params: {
    post: Post;
    style: string;
    brandVoice: string;
    length?: string;
    emojiEnabled?: boolean;
  }) => Promise<unknown>;
  onAddToQueue: (postId: string) => Promise<unknown>;
  onApproveDraft: (draftId: string) => Promise<unknown>;
  isGeneratingDrafts: boolean;
  brandVoice: string;
  autoGeneratedDrafts?: Record<string, Draft[]>;
  autoGeneratingPostId?: string | null;
  isAutoGenerating?: boolean;
}

function getScoreColor(score: number): string {
  if (score >= 75) return 'bg-green-900/50 text-green-300 border-green-800';
  if (score >= 50) return 'bg-yellow-900/50 text-yellow-300 border-yellow-800';
  if (score >= 25) return 'bg-orange-900/50 text-orange-300 border-orange-800';
  return 'bg-red-900/50 text-red-300 border-red-800';
}

function getStatusColor(status: string): string {
  switch (status) {
    case 'SCORED': return 'bg-blue-900/40 text-blue-300';
    case 'DRAFTED': return 'bg-purple-900/40 text-purple-300';
    case 'APPROVED': return 'bg-green-900/40 text-green-300';
    case 'READY_FOR_REVIEW': return 'bg-indigo-900/40 text-indigo-300';
    case 'POSTED': return 'bg-gray-800 text-gray-400';
    case 'SKIPPED': return 'bg-gray-800 text-gray-500';
    default: return 'bg-gray-800 text-gray-400';
  }
}

function formatFollowers(count: number): string {
  if (!count || count <= 0) return '—';
  if (count >= 1000000) return `${(count / 1000000).toFixed(1)}M`;
  if (count >= 1000) return `${(count / 1000).toFixed(1)}K`;
  return String(count);
}

interface PostCardProps {
  result: RankingResult;
  onGenerateDrafts: PostListProps['onGenerateDrafts'];
  onAddToQueue: PostListProps['onAddToQueue'];
  onApproveDraft: PostListProps['onApproveDraft'];
  isGeneratingDrafts: boolean;
  brandVoice: string;
  preloadedDrafts?: Draft[];
  isAutoGeneratingThis?: boolean;
}

function PostCard({ result, onGenerateDrafts, onAddToQueue, onApproveDraft, isGeneratingDrafts, brandVoice, preloadedDrafts, isAutoGeneratingThis }: PostCardProps) {
  const { post, score, keyword_matches } = result;
  const [expanded, setExpanded] = useState(false);
  const [showDrafts, setShowDrafts] = useState(false);
  const [drafts, setDrafts] = useState<Draft[]>([]);
  const [isGenerating, setIsGenerating] = useState(false);

  // Show auto-generated drafts when they arrive
  useEffect(() => {
    if (preloadedDrafts && preloadedDrafts.length > 0) {
      setDrafts(preloadedDrafts);
      setShowDrafts(true);
    }
  }, [preloadedDrafts]);
  const [style, setStyle] = useState('professional');
  const [generationError, setGenerationError] = useState('');
  const [isEditing, setIsEditing] = useState(false);
  const [editText, setEditText] = useState(post.post_text);
  const [isSaving, setIsSaving] = useState(false);
  const [currentText, setCurrentText] = useState(post.post_text);

  // 快速留言
  const [showQuickReply, setShowQuickReply] = useState(false);
  const [quickText, setQuickText] = useState('已追！');
  const [quickStatus, setQuickStatus] = useState<'idle' | 'sending' | 'success' | 'error'>('idle');
  const [quickError, setQuickError] = useState('');

  const truncatedText = currentText.length > 180
    ? currentText.substring(0, 180) + '...'
    : currentText;

  const handleSaveEdit = async () => {
    if (!editText.trim() || editText === currentText) { setIsEditing(false); return; }
    setIsSaving(true);
    try {
      await updatePost(post.id, editText);
      setCurrentText(editText);
      setIsEditing(false);
    } catch (err) {
      console.error('Update post failed:', err);
    } finally {
      setIsSaving(false);
    }
  };

  const handleCancelEdit = () => {
    setEditText(currentText);
    setIsEditing(false);
  };

  const handleQuickReply = async () => {
    if (!quickText.trim()) return;
    setQuickStatus('sending');
    setQuickError('');
    try {
      const res = await radarExecuteDirect(post.id, 'comment', post.author_handle, quickText.trim());
      if (res.success) {
        setQuickStatus('success');
        setShowQuickReply(false);
      } else {
        setQuickStatus('error');
        setQuickError(res.error || '送出失敗');
      }
    } catch (err: any) {
      setQuickStatus('error');
      setQuickError(err.message || '送出失敗');
    }
  };

  const handleGenerateDrafts = async () => {
    setIsGenerating(true);
    setGenerationError('');
    try {
      const result = await onGenerateDrafts({
        post,
        style,
        brandVoice,
        length: 'medium',
        emojiEnabled: false,
      }) as { drafts: Draft[] };
      if (result && result.drafts) {
        setDrafts(result.drafts);
        setShowDrafts(true);
      }
    } catch (err) {
      setGenerationError(err instanceof Error ? err.message : '草稿生成失敗');
    } finally {
      setIsGenerating(false);
    }
  };

  return (
    <div className="bg-gray-800 border border-gray-700 rounded-lg overflow-hidden">
      {/* Post Header */}
      <div className="p-3">
        <div className="flex items-start justify-between gap-2 mb-2">
          <div className="flex items-center gap-2 min-w-0">
            <div className="w-7 h-7 rounded-full bg-gray-700 flex items-center justify-center flex-shrink-0">
              <User className="w-4 h-4 text-gray-400" />
            </div>
            <div className="min-w-0">
              <span className="text-sm font-medium text-gray-200 truncate block">{post.author_handle}</span>
              <span className="text-xs text-gray-500">{formatFollowers(post.author_followers)} 粉絲</span>
              {post.author_location && (
                <span className="text-xs text-gray-600">· {post.author_location}</span>
              )}
            </div>
          </div>
          <div className="flex items-center gap-1.5 flex-shrink-0">
            <span className={clsx('text-xs font-bold px-2 py-0.5 rounded border', getScoreColor(score))}>
              {score.toFixed(0)}
            </span>
            {isAutoGeneratingThis ? (
              <span className="flex items-center gap-1 px-2 py-0.5 bg-purple-900/40 text-purple-300 rounded text-xs">
                <Loader2 className="w-3 h-3 animate-spin" />
                生成中
              </span>
            ) : (
              <span className={clsx('text-xs px-2 py-0.5 rounded', getStatusColor(post.status))}>
                {post.status}
              </span>
            )}
          </div>
        </div>

        {/* Post Text */}
        {isEditing ? (
          <div className="mt-1">
            <textarea
              value={editText}
              onChange={e => setEditText(e.target.value)}
              rows={6}
              className="w-full bg-gray-900 border border-indigo-600 rounded-lg px-3 py-2 text-sm text-gray-100 focus:outline-none resize-none"
              autoFocus
            />
            <div className="flex gap-2 mt-1.5">
              <button
                onClick={handleSaveEdit}
                disabled={isSaving}
                className="flex items-center gap-1 px-2.5 py-1 bg-indigo-600 hover:bg-indigo-500 disabled:opacity-50 text-white rounded text-xs"
              >
                <Check className="w-3 h-3" />
                {isSaving ? '儲存中...' : '儲存'}
              </button>
              <button
                onClick={handleCancelEdit}
                className="flex items-center gap-1 px-2.5 py-1 bg-gray-700 hover:bg-gray-600 text-gray-300 rounded text-xs"
              >
                <X className="w-3 h-3" />
                取消
              </button>
            </div>
          </div>
        ) : (
          <>
            <p className="text-sm text-gray-300 leading-relaxed">
              {expanded ? currentText : truncatedText}
            </p>
            {currentText.length > 180 && (
              <button
                onClick={() => setExpanded(!expanded)}
                className="flex items-center gap-0.5 text-xs text-indigo-400 hover:text-indigo-300 mt-1 transition-colors"
              >
                {expanded ? (
                  <><ChevronUp className="w-3 h-3" /> 收起</>
                ) : (
                  <><ChevronDown className="w-3 h-3" /> 展開</>
                )}
              </button>
            )}
          </>
        )}

        {/* Keyword Matches */}
        {keyword_matches.length > 0 && (
          <div className="flex flex-wrap gap-1 mt-2">
            {keyword_matches.map((kw) => (
              <span key={kw} className="px-1.5 py-0.5 text-xs bg-indigo-900/30 text-indigo-400 rounded border border-indigo-800/40">
                #{kw}
              </span>
            ))}
          </div>
        )}

        {/* Engagement Stats */}
        <div className="flex items-center gap-3 mt-2 text-xs text-gray-500">
          <span className="flex items-center gap-1">
            <ThumbsUp className="w-3 h-3" />
            {post.like_count}
          </span>
          <span className="flex items-center gap-1">
            <MessageCircle className="w-3 h-3" />
            {post.reply_count}
          </span>
          <span className="flex items-center gap-1">
            <Repeat2 className="w-3 h-3" />
            {post.repost_count}
          </span>
          <div className="ml-auto flex items-center gap-2">
            <button
              onClick={() => { setEditText(currentText); setIsEditing(true); }}
              className="flex items-center gap-0.5 text-gray-500 hover:text-gray-300 transition-colors"
              title="編輯貼文"
            >
              <Pencil className="w-3 h-3" />
              編輯
            </button>
            <a
              href={post.post_url}
              target="_blank"
              rel="noopener noreferrer"
              className="flex items-center gap-0.5 text-indigo-500 hover:text-indigo-400 transition-colors"
            >
              <ExternalLink className="w-3 h-3" />
              查看
            </a>
          </div>
        </div>
      </div>

      {/* Actions */}
      {post.status !== 'SKIPPED' && post.status !== 'POSTED' && (
        <div className="px-3 pb-3 space-y-2">
          {/* 快速留言區 */}
          {quickStatus === 'success' ? (
            <div className="flex items-center gap-1.5 text-xs text-green-400 py-1">
              <CheckCircle2 className="w-3.5 h-3.5" />
              留言已送出
            </div>
          ) : (
            <>
              {!showQuickReply ? (
                <button
                  onClick={() => setShowQuickReply(true)}
                  className="w-full flex items-center justify-center gap-1.5 py-1.5 bg-teal-800/50 hover:bg-teal-700/50 border border-teal-700/50 text-teal-300 rounded text-xs font-medium transition-colors"
                >
                  <Send className="w-3 h-3" />
                  快速留言（已追）
                </button>
              ) : (
                <div className="space-y-1.5">
                  <div className="flex gap-1.5">
                    <input
                      type="text"
                      value={quickText}
                      onChange={e => setQuickText(e.target.value)}
                      onKeyDown={e => e.key === 'Enter' && handleQuickReply()}
                      className="flex-1 px-2 py-1.5 bg-gray-900 border border-teal-700/50 rounded text-xs text-gray-200 focus:outline-none focus:border-teal-500"
                      autoFocus
                    />
                    <button
                      onClick={handleQuickReply}
                      disabled={quickStatus === 'sending' || !quickText.trim()}
                      className="px-2.5 py-1.5 bg-teal-700 hover:bg-teal-600 text-white rounded text-xs font-medium transition-colors disabled:opacity-50"
                    >
                      {quickStatus === 'sending' ? <Loader2 className="w-3.5 h-3.5 animate-spin" /> : <Send className="w-3.5 h-3.5" />}
                    </button>
                    <button
                      onClick={() => { setShowQuickReply(false); setQuickStatus('idle'); setQuickError(''); }}
                      className="px-2 py-1.5 bg-gray-700 hover:bg-gray-600 text-gray-400 rounded text-xs transition-colors"
                    >
                      <X className="w-3.5 h-3.5" />
                    </button>
                  </div>
                  {quickStatus === 'error' && (
                    <div className="flex items-center gap-1 text-xs text-red-400">
                      <XCircle className="w-3 h-3" />
                      {quickError}
                    </div>
                  )}
                </div>
              )}
            </>
          )}

          <div className="flex gap-2">
            <div className="flex-1 flex gap-1.5">
              <select
                value={style}
                onChange={(e) => setStyle(e.target.value)}
                className="px-2 py-1.5 text-xs bg-gray-700 border border-gray-600 rounded text-gray-300 focus:outline-none focus:border-indigo-500"
              >
                <option value="professional">專業</option>
                <option value="casual">輕鬆</option>
                <option value="witty">幽默</option>
                <option value="empathetic">同理心</option>
                <option value="educational">教育性</option>
              </select>
              <button
                onClick={handleGenerateDrafts}
                disabled={isGenerating || isGeneratingDrafts}
                className={clsx(
                  'flex items-center gap-1.5 px-2.5 py-1.5 rounded text-xs font-medium transition-colors flex-1 justify-center',
                  isGenerating || isGeneratingDrafts
                    ? 'bg-gray-700 text-gray-500 cursor-not-allowed'
                    : 'bg-purple-800 hover:bg-purple-700 text-purple-200'
                )}
              >
                <Sparkles className="w-3.5 h-3.5" />
                {isGenerating ? '生成中...' : '生成草稿'}
              </button>
            </div>
            <button
              onClick={() => onAddToQueue(post.id)}
              className="flex items-center gap-1.5 px-2.5 py-1.5 bg-gray-700 hover:bg-gray-600 text-gray-300 rounded text-xs font-medium transition-colors"
            >
              <ListPlus className="w-3.5 h-3.5" />
              加入佇列
            </button>
          </div>
        </div>
      )}

      {generationError && (
        <div className="px-3 pb-3">
          <p className="text-red-400 text-xs">{generationError}</p>
        </div>
      )}

      {/* Drafts Section */}
      {showDrafts && drafts.length > 0 && (
        <div className="border-t border-gray-700 px-3 py-3 space-y-2">
          <h4 className="text-xs font-semibold text-gray-400 uppercase tracking-wider">已生成草稿</h4>
          {drafts.map((draft, index) => (
            <DraftCard
              key={draft.id || index}
              draft={draft}
              onApprove={async () => {
                if (draft.id) {
                  await onApproveDraft(draft.id);
                  setDrafts((prev) => prev.map((d, i) => i === index ? { ...d, approved: true } : d));
                }
              }}
              onRegenerate={handleGenerateDrafts}
              isRegenerating={isGenerating}
            />
          ))}
        </div>
      )}
    </div>
  );
}

export function PostList({ results, isLoading, onGenerateDrafts, onAddToQueue, onApproveDraft, isGeneratingDrafts, brandVoice, autoGeneratedDrafts, autoGeneratingPostId, isAutoGenerating }: PostListProps) {
  if (isLoading) {
    return (
      <div className="space-y-3">
        {[1, 2, 3].map((i) => (
          <div key={i} className="bg-gray-800 border border-gray-700 rounded-lg p-4 animate-pulse">
            <div className="flex items-center gap-2 mb-3">
              <div className="w-7 h-7 rounded-full bg-gray-700" />
              <div className="h-4 bg-gray-700 rounded w-32" />
            </div>
            <div className="space-y-2">
              <div className="h-3 bg-gray-700 rounded w-full" />
              <div className="h-3 bg-gray-700 rounded w-3/4" />
            </div>
          </div>
        ))}
      </div>
    );
  }

  if (results.length === 0) {
    return (
      <div className="flex flex-col items-center justify-center py-16 text-center">
        <div className="w-12 h-12 rounded-full bg-gray-800 flex items-center justify-center mb-4">
          <SearchIcon className="w-6 h-6 text-gray-600" />
        </div>
        <p className="text-gray-500 text-sm">尚無貼文。</p>
        <p className="text-gray-600 text-xs mt-1">執行搜尋以發現貼文</p>
      </div>
    );
  }

  const generatedCount = autoGeneratedDrafts ? Object.keys(autoGeneratedDrafts).length : 0;

  return (
    <div className="space-y-3">
      <div className="flex items-center justify-between">
        <p className="text-xs text-gray-500">共 {results.length} 篇貼文，依相關度排名</p>
        {isAutoGenerating && (
          <span className="flex items-center gap-1.5 text-xs text-purple-300">
            <Loader2 className="w-3 h-3 animate-spin" />
            自動生成草稿 {generatedCount}/{results.length}
          </span>
        )}
        {!isAutoGenerating && generatedCount > 0 && (
          <span className="text-xs text-purple-400">{generatedCount} 份草稿已生成</span>
        )}
      </div>
      {results.map((result) => (
        <PostCard
          key={result.post.id}
          result={result}
          onGenerateDrafts={onGenerateDrafts}
          onAddToQueue={onAddToQueue}
          onApproveDraft={onApproveDraft}
          isGeneratingDrafts={isGeneratingDrafts}
          brandVoice={brandVoice}
          preloadedDrafts={autoGeneratedDrafts?.[result.post.id]}
          isAutoGeneratingThis={autoGeneratingPostId === result.post.id}
        />
      ))}
    </div>
  );
}

