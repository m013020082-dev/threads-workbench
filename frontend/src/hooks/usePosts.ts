import { useState, useCallback } from 'react';
import { useMutation, useQueryClient } from '@tanstack/react-query';
import { Post, Draft, RankingResult, SearchParams } from '../types';
import { searchPosts, generateDrafts, getDraftsForPost, addToQueue } from '../api/client';

export function usePosts(workspaceId: string | null) {
  const queryClient = useQueryClient();
  const [searchResults, setSearchResults] = useState<RankingResult[]>([]);
  const [selectedPost, setSelectedPost] = useState<Post | null>(null);
  const [isSearching, setIsSearching] = useState(false);
  const [searchError, setSearchError] = useState<string | null>(null);

  const handleSearch = useCallback(
    async (params: Omit<SearchParams, 'workspace_id'>): Promise<RankingResult[]> => {
      if (!workspaceId) {
        setSearchError('Please select a workspace first');
        return [];
      }

      setIsSearching(true);
      setSearchError(null);

      try {
        const result = await searchPosts({
          ...params,
          workspace_id: workspaceId,
        });
        setSearchResults(result.results);
        return result.results;
      } catch (err) {
        setSearchError(err instanceof Error ? err.message : 'Search failed');
        setSearchResults([]);
        return [];
      } finally {
        setIsSearching(false);
      }
    },
    [workspaceId]
  );

  // Auto-generate drafts for all results sequentially after search
  const [autoGeneratedDrafts, setAutoGeneratedDrafts] = useState<Record<string, Draft[]>>({});
  const [autoGeneratingPostId, setAutoGeneratingPostId] = useState<string | null>(null);
  const [isAutoGenerating, setIsAutoGenerating] = useState(false);

  const autoGenerateAll = useCallback(
    async (
      results: RankingResult[],
      params: { postingLogic: string; replyNote: string; brandVoice: string }
    ) => {
      if (!workspaceId || results.length === 0) return;
      setIsAutoGenerating(true);
      setAutoGeneratedDrafts({});

      for (const { post } of results) {
        setAutoGeneratingPostId(post.id);
        try {
          const result = await generateDrafts(
            post.id,
            post.post_text,
            'professional',
            params.brandVoice,
            workspaceId,
            'medium',
            false,
            params.postingLogic,
            params.replyNote
          );
          if (result.drafts && result.drafts.length > 0) {
            setAutoGeneratedDrafts((prev) => ({ ...prev, [post.id]: result.drafts as Draft[] }));
            setSearchResults((prev) =>
              prev.map((r) =>
                r.post.id === post.id
                  ? { ...r, post: { ...r.post, status: 'DRAFTED' as Post['status'] } }
                  : r
              )
            );
          }
        } catch {
          // skip failed posts, continue with next
        }
      }

      setAutoGeneratingPostId(null);
      setIsAutoGenerating(false);
    },
    [workspaceId]
  );

  // Generate drafts mutation
  const generateDraftsMutation = useMutation({
    mutationFn: async ({
      post,
      style,
      brandVoice,
      length,
      emojiEnabled,
      postingLogic,
      replyNote,
    }: {
      post: Post;
      style: string;
      brandVoice: string;
      length?: string;
      emojiEnabled?: boolean;
      postingLogic?: string;
      replyNote?: string;
    }) => {
      if (!workspaceId) throw new Error('No workspace selected');
      return generateDrafts(
        post.id,
        post.post_text,
        style,
        brandVoice,
        workspaceId,
        length || 'medium',
        emojiEnabled || false,
        postingLogic || '擬人幽默',
        replyNote || ''
      );
    },
    onSuccess: (_, variables) => {
      queryClient.invalidateQueries({ queryKey: ['drafts', variables.post.id] });
      // Update search results to show DRAFTED status
      setSearchResults((prev) =>
        prev.map((r) =>
          r.post.id === variables.post.id
            ? { ...r, post: { ...r.post, status: 'DRAFTED' as Post['status'] } }
            : r
        )
      );
    },
  });

  // Get drafts query
  const getDrafts = useCallback(
    (postId: string) => {
      return queryClient.fetchQuery({
        queryKey: ['drafts', postId],
        queryFn: () => getDraftsForPost(postId),
        staleTime: 5000,
      });
    },
    [queryClient]
  );

  // Add to queue mutation
  const addToQueueMutation = useMutation({
    mutationFn: async (postId: string) => {
      return addToQueue(postId);
    },
    onSuccess: (_, postId) => {
      queryClient.invalidateQueries({ queryKey: ['queue', workspaceId] });
      setSearchResults((prev) =>
        prev.map((r) =>
          r.post.id === postId
            ? { ...r, post: { ...r.post, status: 'READY_FOR_REVIEW' as Post['status'] } }
            : r
        )
      );
    },
  });

  return {
    searchResults,
    selectedPost,
    setSelectedPost,
    isSearching,
    searchError,
    handleSearch,
    generateDrafts: generateDraftsMutation.mutateAsync,
    isGeneratingDrafts: generateDraftsMutation.isPending,
    getDrafts,
    addToQueue: addToQueueMutation.mutateAsync,
    isAddingToQueue: addToQueueMutation.isPending,
    autoGenerateAll,
    autoGeneratedDrafts,
    autoGeneratingPostId,
    isAutoGenerating,
  };
}
